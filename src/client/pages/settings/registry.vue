<template>
<FormBase>
	<FormGroup v-if="scopes">
		<template #label>{{ $ts.system }}</template>
		<FormLink v-for="scope in scopes" :to="`/settings/registry/keys/system/${scope.join('/')}`">{{ scope.join('/') }}</FormLink>
	</FormGroup>
</FormBase>
</template>

<script lang="ts">
import { defineAsyncComponent, defineComponent } from 'vue';
import { faCogs } from '@fortawesome/free-solid-svg-icons';
import MkInfo from '@/components/ui/info.vue';
import FormSwitch from '@/components/form/switch.vue';
import FormSelect from '@/components/form/select.vue';
import FormLink from '@/components/form/link.vue';
import FormBase from '@/components/form/base.vue';
import FormGroup from '@/components/form/group.vue';
import FormButton from '@/components/form/button.vue';
import FormKeyValueView from '@/components/form/key-value-view.vue';
import * as os from '@/os';

export default defineComponent({
	components: {
		MkInfo,
		FormBase,
		FormSelect,
		FormSwitch,
		FormButton,
		FormLink,
		FormGroup,
		FormKeyValueView,
	},

	emits: ['info'],
	
	data() {
		return {
			INFO: {
				title: this.$ts.registry,
				icon: faCogs
			},
			scopes: null,
		}
	},

	created() {
		os.api('i/registry/scopes').then(scopes => {
			this.scopes = scopes;
		});
	},

	mounted() {
		this.$emit('info', this.INFO);
	},
});
</script>
